// markdown 正则匹配规则
var newline = /^\n+/m; // 空行

var hr = /^ {0,3}((?:- *){3,}|(?:_ *){3,}|(?:\* *){3,})(?:\n+|$)/m; // 分隔符 --- *** ___

var heading = /^ {0,3}(#{1,6}) +([^\n]*?)(?: +#+)? *(\n|$)/m; // ATX 标题 ######

var code = /^(\s{4}[^\n]+\n*)+/m; // 缩进代码块

var fence = /^ {0,3}(`{3,}(?=[^`\n]*\n)|~{3,})([^\n]*)\n([\s\S]*?\n)(?: {0,3}\1[`~]* *(?:\n+|$)|$)/m; // 围栏代码块

var lheading = /^([^\n]+)\n {0,3}(=+|-+) *(?:\n+|$)/m; // Setext 标题 === ---
